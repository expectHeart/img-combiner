package com.github.xuefm.enums;

/**
 * 矩形类型
 */
public enum RectangleType {

    /**
     * 矩形(轮廓)
     */
    DrawRect,

    /**
     * 矩形(填充)
     */
    FillRect,

    /**
     * 3D矩形(轮廓)
     */
    Draw3DRect,

    /**
     * 3D矩形(填充)
     */
    Fill3DRect,

    /**
     * 圆角矩形(轮廓)
     */
    DrawRoundRect,

    /**
     * 圆角矩形(填充)
     */
    FillRoundRect,


}
